<?php
// Создаем массив из 20 случайных чисел в диапазоне [-20, 50]
$arr = [];
for ($i = 0; $i < 20; $i++) {
    $arr[] = rand(-20, 50);
}

// Генерируем случайные числа a и b в диапазоне [-20, 50]
$a = rand(-20, 50);
$b = rand(-20, 50);

// Получаем новый массив, содержащий только числа из arr в диапазоне от a до b
$newArr = [];
foreach ($arr as $num) {
    if ($num >= $a && $num <= $b) {
        $newArr[] = $num;
    }
}

// Выводим результаты на экран
echo "<p>Исходный массив: " . implode(", ", $arr) . "</p>";
echo "<p>Границы диапазона: [$a, $b]</p>";
if (count($newArr) > 0) {
    echo "<p>Новый массив: " . implode(", ", $newArr) . "</p>";
} else {
    echo "<p>В заданном диапазоне нет чисел из исходного массива.</p>";
}
?>

